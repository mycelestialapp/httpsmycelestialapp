function matchTopic(question: string, rule: TopicRule): boolean {
  const q = question.trim();
  if (!q) return false;
  if (rule.pattern instanceof RegExp) return rule.pattern.test(q);
  return rule.pattern.some((kw) => q.includes(kw));
}

/**
 * 依問題與分類，從主題表中取第一條命中的指引；若無則回傳 null，由呼叫方用大類預設。
 */
export function getTopicGuidance(question: string, category: QuestionCategory): string | null {
  const rules = GUIDANCE_TOPICS[category];
  if (!rules?.length) return null;
  const hit = rules.find((r) => matchTopic(question, r));
  return hit ? hit.guidance : null;
}
